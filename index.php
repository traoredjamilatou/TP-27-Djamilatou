<div class="M">
<p>Situées à environ 12 km au nord-ouest de Banfora, elles tiennent leur nom de la commune de Karfiguéla. Elles constituent <br>  l'un des sites touristiques les plus importants au Burkina Faso. La région des Cascades tire son nom de ces chutes d'eau.</p>
<p>Banfora, ville frontalière de la Côte d’Ivoire dispose d’un climat doux et d’une végétation comparable à celle des pays <br> côtiers permettant de le nommé sans en abuser la Côte d’Azur du Burkina. Mais au sommet d’une colline, se laissent admirer <br> les fameuses cascades qui font la fierté de tout Banfora.</p>
<p>Les cascades de Karfiguéla ou cascades de Banfora sont une série de cascades le long du fleuve Comoé au sud-ouest du <br> Burkina Faso. Elles sont situées à environ 12 km au nord-ouest de Banfora et constituent l’un des sites touristiques les plus <br> importants au Burkina Faso. La région des Cascades tire son nom de ces cascades. Dans la chaleur de ce mois de mai, <br> entendre le son de l’eau qui coule au loin est en soi une délivrance. Une dizaine de baignoires naturelles et de bains à <br> remous remplis d’une eau limpide et glacée vous y attendent.</p>
<p>La visite des lieux est payante : 1000F CFA pour les étrangers et 500F CFA pour les visiteurs nationaux. La seule stricte <br> interdiction est de passer la nuit sur le site.</p>
